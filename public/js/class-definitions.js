/* Step 83
 *
 * Declare a Shape method called getType that returns a string
 * containing the type of shape based on the sides property.
 *
 *       this.sides => 
 *                3 => "triangle"
 *                4 => "quadrilateral"
 *                5 => "pentagon"
 *                6 => "hexagon"
 *                7 => "heptagon"
 *                8 => "octagon"
 *                9 => "nonagon"
 *               10 => "decagon"
 * Any other number => "Could not determine type"
 *
 */
Shape.prototype.getType = function() {
  var result;
  switch(this.sides) {
    case 3: {
      result = "triangle";
      break;
    }
    case 4 : {
      result = "quadrilateral";
      break;
    }
    case 5 : {
      result = "pentagon";
      break;
    }
    case 6 : {
      result = "hexagon";
      break;
    }
    case 7 : {
      result = "heptagon";
      break;
    }
    case 8 : {
      result = "octagon";
      break;
    }
    case 9 : {
      result = "nonagon";
      break;
    }
    case 10 : {
      result = "decagon";
      break;
    }
    default:
      result = "Could not determine type";
  }
  return result;
};

